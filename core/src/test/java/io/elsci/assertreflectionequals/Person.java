package io.elsci.assertreflectionequals;

public class Person {
    long id;
    short age;
    double weight;
    int height;
    byte shoeSize;
    float waist;

    public Person(long id, short age, double weight, int height, byte shoeSize, float waist) {
        this.id = id;
        this.age = age;
        this.weight = weight;
        this.height = height;
        this.shoeSize = shoeSize;
        this.waist = waist;
    }
}
